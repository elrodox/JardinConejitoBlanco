                    <li>SISMOS:</li>
                    <p>Alerta y alarma: Se hace contacto con Bomberos y Carabineros para apoyo humano en el desalojo de los pre-escolares hacia la zona segura.
                    <br>Instrucciones:</br></p>
                    <ul>
                        <li>Permanecer en el recinto, no salir al exterior.</li>
                        <li>Las Educadoras mantendrán la calma y organizarán a los pre-escolares.</li>
                        <li>La Educadora o Asistente más cercana a la puerta procede a abrirla.</li>
                        <li>Retirarse lejos de las ventanas.</li>
                        <li>Al producirse desprendimiento de objetos, deben instalarse bajo el mobiliario.</li>
                        <li>En caso de ruptura de luces fluorescentes o similares, evacuar lo antes posible.</li>
                        <li>No evacuar durante el Sismo.</li>
                        <li>Finalizado el sismo, se procede la inmediata evacuación del recinto hacia la zona de seguridad cubriendo la cabeza con artículos o las manos.</li>
                        <li>Sólo personal de bomberos y expertos podrán evaluar las condiciones de habitabilidad del recinto.</li>
                    </ul>
                    <li>INCENDIOS:</li>
                    <p>Alerta y alarma: Se hace contacto con personal de la Escuela de Trabajo Social para apoyo humano en el desalojo y traslado de los pre-escolares.
                    <br>Instrucciones</br></p>
                    <ul>
                        <li>Al producirse principio de incendio, se evacua las salas a la zona se seguridad por fuerza mayor desalojarán el recinto por el portón.</li>
                        <li>Se da aviso a Bomberos y Carabineros.</li>
                        <li>Las personas asignadas a los extintores proceden a actuar inmediatamente o su reemplazante designado de no estar presente, recuerde que sólo debe actuar sólo con la carga existente y retirarse a la zona de seguridad.</li>
                        <li>No abrir puertas ni ventanas, si no fuese necesario.</li>
                    </ul>
                    
                    <li>ESCAPE DE GAS:</li>
                    <p>Alerta y alarma: Se hace contacto con personal de la Escuela de Trabajo Social para apoyo humano en el desalojo de los pre-escolares y trasladarlos del recinto.
                    <br>Instrucciones:</br></p>
                    <ul>
                        <li>Se evacuan las salas del primer piso por la entrada principal hacia la zona segura, por ningún motivo por el sector de los depósitos de gas licuado.</li>
                        <li>Las manipuladoras de alimentos realizan el corte de gas central.</li>
                        <li>Se da aviso a Bomberos y Carabineros.</li>
                        <li>No encender fósforos ni fuego de ningún tipo.</li>
                        <li>Los expertos evaluarán, inspeccionarán y repararán.</li>
                    </ul>
                    <li>MANIFESTACIONES ESTUDIANTILES:</li>
                    <ul>
                        <li>Cuando la manifestación es anunciada, procurar que los niños no se encuentren en el lugar a la hora de la marcha.</li>
                        <li>Cuando es improvisada, manténgase atento a las instrucciones.</li>
                        <li>Cierre válvulas, apague equipos.</li>
                        <li>Mantener a los párvulos en sus salas.</li>
                        <li>Si es necesario cubrir la cara con un pañuelo o papel, y mantener cerradas puertas y ventanas.</li>
                        <li>Jamás se moje la cara o mucosa.</li>
                        <li>De ser posible, solicitar a los padres que retiren a sus hijos que evacuen a favor del viento y cierre.</li>
                        <li>Si se sofoca siga respirando normalmente.</li>
                        <li>Si no puede salir de la sede, diríjase y manténgase en un lugar protegido.</li>
                        <li>Evalúe la naturaleza y magnitud del problema.</li>
                        <li>Espere las instrucciones.</li>
                    </ul>
                    <li>AMENAZA DE BOMBA:</li>
                    <ul>
                        <li>De la alarma a la persona encargada.</li>
                        <li>El encargado deberá llamar a carabineros, bomberos y ambulancia.</li>
                        <li>Si es posible corte los suministros.</li>
                        <li>Proceder con el Plan de Evacuación.</li>
                        <li>Evacue en forma ordenada y aléjese rápidamente de la sede.</li>
                    </ul>
